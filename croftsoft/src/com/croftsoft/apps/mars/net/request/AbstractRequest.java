     public abstract class  AbstractRequest
       implements Request
     //////////////////////////////////////////////////////////////////////
     //////////////////////////////////////////////////////////////////////
     {

     private final String  playerName;

     //////////////////////////////////////////////////////////////////////
     //////////////////////////////////////////////////////////////////////

     public  AbstractRequest ( String  playerName )
     //////////////////////////////////////////////////////////////////////
     {
       this.playerName = playerName;
     }

     //////////////////////////////////////////////////////////////////////
     //////////////////////////////////////////////////////////////////////

     public String  getPlayerName ( ) { return playerName; }

     //////////////////////////////////////////////////////////////////////
     //////////////////////////////////////////////////////////////////////
     }
