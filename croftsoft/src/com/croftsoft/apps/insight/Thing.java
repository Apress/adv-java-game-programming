     package com.croftsoft.apps.insight;

     /*********************************************************************
     * Data for a kobold, goblin, or wall.
     *
     * @version
     *   2002-03-02
     * @since
     *   2001-03-02
     * @author
     *   <a href="http://www.croftsoft.com/">David Wallace Croft</a>
     *********************************************************************/

     public final class  Thing
     //////////////////////////////////////////////////////////////////////
     //////////////////////////////////////////////////////////////////////
     {

     public int      type;

     public int      x;

     public int      y;

     public boolean  isAlive;

     //////////////////////////////////////////////////////////////////////
     //////////////////////////////////////////////////////////////////////

     public  Thing (
       int  type,
       int  x,
       int  y )
     //////////////////////////////////////////////////////////////////////
     {
       this.type = type;

       this.x    = x;

       this.y    = y;

       isAlive   = true;
     }

     public  Thing ( int  type )
     //////////////////////////////////////////////////////////////////////
     {
       this ( type, 0, 0 );
     }

     //////////////////////////////////////////////////////////////////////
     //////////////////////////////////////////////////////////////////////
     }