     package com.croftsoft.core.util.state;

     import com.croftsoft.core.role.Consumer;

     /*********************************************************************
     *
     * Adapts the StateListener interface to the Consumer interface.
     *
     * @author
     *   <A HREF="http://www.alumni.caltech.edu/~croft/">David W. Croft</A>
     * @version
     *   1998-12-05
     *********************************************************************/

     public class  StateListenerConsumer implements Consumer
     //////////////////////////////////////////////////////////////////////
     //////////////////////////////////////////////////////////////////////
     {

     protected StateListener  stateListener;

     //////////////////////////////////////////////////////////////////////
     //////////////////////////////////////////////////////////////////////

     public  StateListenerConsumer ( StateListener  stateListener )
     //////////////////////////////////////////////////////////////////////
     {
       this.stateListener = stateListener;
     }

     //////////////////////////////////////////////////////////////////////
     //////////////////////////////////////////////////////////////////////

     public void  consume ( Object  o )
     //////////////////////////////////////////////////////////////////////
     {
       stateListener.stateListen ( ( State ) o );
     }

     //////////////////////////////////////////////////////////////////////
     //////////////////////////////////////////////////////////////////////
     }
